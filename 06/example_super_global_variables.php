// they are used to access data from different sources, such as form data, session data, and server information
// super global variables are available in all scopes throughout a script
// list of super global variables
// $GLOBALS - an associative array containing all global variables
// $_SERVER - an associative array containing server and execution environment information
// $_REQUEST - an associative array containing data from GET, POST, and COOKIE methods
// $_POST - an associative array containing data from the POST method
// $_GET - an associative array containing data from the GET method
// $_FILES - an associative array containing file upload information
// $_ENV - an associative array containing environment variables
// $_COOKIE - an associative array containing cookie data
